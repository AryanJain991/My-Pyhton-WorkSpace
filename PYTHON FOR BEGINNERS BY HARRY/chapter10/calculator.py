a = int(input("enter value of a:"))
b = int(input("enter value of b:"))
ob = input("enter operator c:")

if ob == "+":
    n1 = a  +  b
    print(f"addition is: {n1}")

elif ob == "-":
    n2 = a - b
    print(f"addition is: {n2}")

elif ob == "*":
    n2 = a * b
    print(f"addition is: {n2}")

elif ob == "/":
    n2 = a / b
    print(f"addition is: {n2}")

else:
    print("not valid")
