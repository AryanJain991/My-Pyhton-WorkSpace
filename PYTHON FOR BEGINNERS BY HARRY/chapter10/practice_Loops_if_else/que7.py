# Question 7: Write a program that takes the length and width of a rectangle and calculates its area and perimeter.

width = int(input("Pls Enter The Width: "))
length = int(input("Pls Enter The Length: "))

area = width * length
print(f"Area For Rectangle Is: {area}") 

parimeter = 2*(width + length)
print(f"Parimeter For Rectangle Is: {parimeter}") 